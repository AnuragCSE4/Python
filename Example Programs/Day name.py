option = int(input("Enter any number between 0 to 6 of the day : "))
if option == 0 :
    print("It's Sunday")
elif option == 1 :
    print("It's Monday")
elif option == 2 :
    print("It's Tuesday")
elif option == 3 :
    print("It's Wednesday")
elif option == 4 :
    print("It's Thursday")
elif option == 5 :
    print("It's Friday")
elif option == 6 :
    print("It's Saturday")
else :
    print("It's wrong input")